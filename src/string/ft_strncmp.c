#include "ft_type.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	while (n && *s1 && *s1 == *s2)
	{
		++s1;
		++s2;
		--n;
	}
	return (n ? (t_u8)(*s1) - (t_u8)(*s2) : 0);
}
