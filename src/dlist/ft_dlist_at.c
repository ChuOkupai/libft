#include "ft_dlist.h"

t_dlist	*ft_dlist_at(t_dlist *l, int i)
{
	int d;

	d = i > 0 ? -1 : 1;
	while (i && l)
	{
		l = i < 0 ? l->prev : l->next;
		i += d;
	}
	return (l);
}
