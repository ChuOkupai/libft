#include <libft.h>

int	main(void)
{
	int		i;
	char	c;

	i = -1;
	while (++i < 128)
	{
		c = i;
		ft_print_char(&c);
		ft_putchar(i % 8 == 7 ? '\n' : '\t');
	}
	return (0);
}
