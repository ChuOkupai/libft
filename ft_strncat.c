#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char *d;

	d = s1 + ft_strlen(s1);
	while (n && *s2)
	{
		*d++ = *s2++;
		n--;
	}
	*d = '\0';
	return (s1);
}
